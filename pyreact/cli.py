def preprocess_ai_directives(source: str) -> str:
    """Pre-processes any #ai "..." comments in the file by calling the local generator."""
    import re
    pattern = r'#ai\s+["\'](.*?)["\']'
    matches = re.findall(pattern, source)
    if not matches:
        return source
        
    print(f"  [AI] Found {len(matches)} Natural Language Programming block(s). Translating...")
    from pyreact.compiler.ai import call_local_llm
    
    new_source = source
    for prompt in matches:
        system_prompt = (
            "You are a code translator. Translate the user's natural language request "
            "into valid PyReact JSX elements or Python handler code. Return ONLY raw code without formatting, "
            "markdown, or explanations."
        )
        try:
            print(f"    - Translating: \"{prompt}\" via local LLM...")
            translated = call_local_llm(prompt, system_prompt)
        except Exception:
            # Fallback based on simple keywords if offline
            print(f"    - Translating: \"{prompt}\" (Offline fallback)...")
            prompt_l = prompt.lower()
            if "delete" in prompt_l or "button" in prompt_l:
                translated = '<UI.Button onClick={() => console.log("deleted")}>Delete Item</UI.Button>'
            elif "list" in prompt_l:
                translated = '<ul>{["Item 1", "Item 2"].map(x => <li key={x}>{x}</li>)}</ul>'
            else:
                translated = f'<UI.Text>Translated: {prompt}</UI.Text>'
        
        # Replace comment
        new_source = new_source.replace(f'#ai "{prompt}"', translated).replace(f"#ai '{prompt}'", translated)
        
    return new_source


def _compile(entry: str, out_dir: str = "dist", target: str = "flask",
             run_ssg: bool = False, run_audit: bool = False,
             run_healing: bool = False, heal_model: str = None,
             show_diff: bool = True, no_backup: bool = False):
    """Run the PYREACT compiler pipeline with auditing, auto-healing, and multi-target compilation."""
    sys.path.insert(0, str(Path(__file__).parent))
    from pyreact.compiler.lexer import Lexer, LexerError
    from pyreact.compiler.parser import Parser, ParseError
    from pyreact.compiler.codegen import CodeGenerator

    source = Path(entry).read_text(encoding="utf-8")

    if run_audit:
        from pyreact.compiler.audit import SecurityAuditEngine
        print(f"  [AUDIT] Auditing security of {entry}...")
        warnings = SecurityAuditEngine(entry, source).audit()
        if warnings:
            print(f"\n  [WARN] Security Audit Report - {len(warnings)} warning(s) found:\n")
            for w in warnings:
                loc = f"Line {w['line']}" if w['line'] > 0 else "Config Level"
                print(f"    [{w['severity']}] {w['type']} at {loc}:")
                print(f"      {w['msg']}\n")
        else:
            print("  [OK] Security Audit passed! No issues detected.\n")

    source = preprocess_ai_directives(source)

    print(f"  [1/3] Lexing {entry}...")
    try:
        tokens = Lexer(source).tokenize()
        print(f"  [2/3] Parsing AST...")
        ast_tree = Parser(tokens).parse()
    except (ParseError, LexerError, Exception) as pe:
        if run_healing:
            from pyreact.compiler.healer import auto_heal
            success, healed_code = auto_heal(
                entry=entry,
                source=source,
                error=pe,
                model=heal_model,
                show_diff=show_diff,
                make_backup=not no_backup,
            )
            if not success or healed_code is None:
                sys.exit(1)
            # Re-compile healed code
            print(f"  [2/3] Re-parsing healed source...")
            try:
                tokens    = Lexer(healed_code).tokenize()
                ast_tree  = Parser(tokens).parse()
            except Exception as e2:
                print(f"  [X]  Healed code masih gagal: {e2}")
                sys.exit(1)
        else:
            err_line = getattr(pe, "line", 0)
            err_col  = getattr(pe, "col", 0)
            lines    = source.splitlines()
            print(f"\n  X  Compilation Error in {entry}:\n")
            if err_line and err_line - 1 < len(lines):
                if err_line - 2 >= 0:
                    print(f"  {err_line - 1} | {lines[err_line - 2]}")
                print(f"  {err_line} | {lines[err_line - 1]}")
                if err_col:
                    indent = " " * (len(str(err_line)) + 3 + err_col - 1)
                    print(f"{indent}^")
                if err_line < len(lines):
                    print(f"  {err_line + 1} | {lines[err_line]}")
            print(f"\n  Error: {pe}")
            print(f"  TIP:  Jalankan dengan --heal untuk perbaikan otomatis via AI\n")
            sys.exit(1)

    print(f"  [3/3] Generating code -> {out_dir}/  (Target: {target.upper()})")
    CodeGenerator(ast_tree, out_dir, target).generate()


