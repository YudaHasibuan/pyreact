def cmd_test(args):
    """Run backend unit tests, frontend component snapshots, and E2E playwright tests with HTML coverage."""
    print(BANNER)
    print("  [TEST] Initializing Unified PyReact Test Runner...")
    
    entry = "app.pyreact"
    out_dir = "dist"
    target = "flask"
    
    for i, a in enumerate(args):
        if a == "--out" and i + 1 < len(args): out_dir = args[i + 1]
        if a == "--target" and i + 1 < len(args): target = args[i + 1]
        
    watch_mode = "--watch" in args

    if not Path(entry).exists():
        pyreact_files = list(Path(".").glob("*.pyreact"))
        if pyreact_files:
            entry = str(pyreact_files[0])
        else:
            print("  X  No PyReact entry file (e.g. app.pyreact) found.")
            sys.exit(1)

    def _run_once():
        print(f"  [TEST] Parsing {entry} to build AST...")
        from pyreact.compiler.lexer import Lexer
        from pyreact.compiler.parser import Parser
        from pyreact.compiler.tester import run_testing_suite

        try:
            source = Path(entry).read_text(encoding="utf-8")
            lexer = Lexer(source)
            tokens = lexer.tokenize()
            parser = Parser(tokens)
            ast = parser.parse()
        except Exception as pe:
            from pyreact.compiler.parser import ParseError
            from pyreact.compiler.lexer import LexerError
            if isinstance(pe, (ParseError, LexerError)):
                from pyreact.compiler.errors import format_compilation_error
                err_line = getattr(pe, "line", 1)
                err_col  = getattr(pe, "col", 1)
                suggestion = getattr(pe, "suggestion", "")
                err_msg = str(pe)
                formatted = format_compilation_error(err_msg, err_line, err_col, source, suggestion)
                print(formatted)
            else:
                print(f"  X  Parser Error: {pe}")
            return False
        return True

    if not watch_mode:
        _run_once()
        return

    # --watch mode: auto-rerun on file changes
    print("  [watch] Watching .pyreact files for changes... (Ctrl+C to stop)\n")
    import time
    import hashlib

    def _file_hash(path):
        try:
            return hashlib.md5(Path(path).read_bytes()).hexdigest()
        except Exception:
            return ""

    watched = {str(p): _file_hash(p) for p in Path('.').rglob('*.pyreact')}
    print(f"  Watching {len(watched)} file(s): {', '.join(watched.keys())}\n")
    _run_once()

    try:
        while True:
            time.sleep(1)
            changed = False
            current = {str(p): _file_hash(p) for p in Path('.').rglob('*.pyreact')}
            for path, h in current.items():
                if watched.get(path) != h:
                    print(f"\n  [watch] Changed: {path} — re-running tests...\n")
                    changed = True
            for path in set(watched.keys()) - set(current.keys()):
                print(f"\n  [watch] Removed: {path}")
                changed = True
            watched = current
            if changed:
                _run_once()
    except KeyboardInterrupt:
        print("\n  [watch] Stopped.")
        return

        sys.exit(1)
        
    print(f"  [TEST] Recompiling project to {out_dir} (target: {target})...")
    _compile(entry, out_dir=out_dir, target=target, no_backup=True, show_diff=False)
    
    print("  [TEST] Running test suites...")
    report = run_testing_suite(Path(out_dir), target, ast)
    
    # E2E Test Execution
    try:
        import playwright
        print("\n  [TEST] Running E2E Playwright integration tests...")
        import socket
        import time
        import threading
        
        def is_port_open(port):
            s = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
            try:
                s.bind(("127.0.0.1", port))
                s.close()
                return True
            except socket.error:
                return False
                
        server_thread = None
        if is_port_open(5000):
            print("  [SERVER] Starting backend server in background for E2E testing...")
            def run_server():
                sys.path.insert(0, str(Path(out_dir) / "backend"))
                try:
                    if target == "fastapi":
                        import uvicorn
                        from app import app
                        uvicorn.run(app, host="127.0.0.1", port=5000, log_level="warning")
                    else:
                        from app import app
                        app.run(host="127.0.0.1", port=5000, debug=False, use_reloader=False)
                except Exception as e:
                    print(f"  [SERVER ERROR] {e}")
                finally:
                    if str(Path(out_dir) / "backend") in sys.path:
                        sys.path.remove(str(Path(out_dir) / "backend"))
                        
            server_thread = threading.Thread(target=run_server, daemon=True)
            server_thread.start()
            time.sleep(2)
            
        import subprocess
        res = subprocess.run(["pytest", str(Path(out_dir) / "backend" / "test_e2e_generated.py")], capture_output=True, text=True)
        if res.returncode == 0:
            print("  [OK] Playwright E2E tests passed successfully!")
            report["e2e"] = "passed"
        else:
            if "playwright not installed" in res.stderr or "playwright not installed" in res.stdout:
                print("  [INFO] Playwright not installed. E2E tests skipped.")
                report["e2e"] = "skipped"
            else:
                print("  [WARN] Playwright E2E tests failed.")
                print(res.stdout + "\n" + res.stderr)
                report["e2e"] = "failed"
    except ImportError:
        print("\n  [INFO] Playwright package not installed. Skipping E2E tests.")
        report["e2e"] = "skipped"
    except Exception as e:
        print(f"\n  [WARN] E2E test runner encountered error: {e}")
        report["e2e"] = "error"
        
    print("\n  =======================================================")
    print("  PYREACT UNIFIED TEST SUMMARY REPORT")
    print("  =======================================================")
    print(f"  Backend Unit Tests:  {report['backend']['passed']}/{report['backend']['total']} Passed (Coverage: {report['backend']['coverage']})")
    print(f"  Component Snapshots: {report['snapshots']['passed']} Passed, {report['snapshots']['failed']} Failed, {report['snapshots']['created']} New")
    print(f"  E2E Integration:     {report['e2e'].upper()}")
    print("  =======================================================")
    
    if report['backend']['failed'] > 0 or report['snapshots']['failed'] > 0 or report['e2e'] == "failed":
        print("  [FAILURE] Unified test run failed.")
        sys.exit(1)
    else:
        print("  [SUCCESS] All checks completed successfully!")


