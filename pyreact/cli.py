def cmd_build(args):
    entry = "app.pyreact"
    out_dir = "dist"
    analyze = "--analyze" in args
    for i, a in enumerate(args):
        if a == "--out" and i + 1 < len(args):
            out_dir = args[i + 1]
        elif a.endswith(".pyreact"):
            entry = a

    if not Path(entry).exists():
        print(f"  X  File not found: {entry}")
        sys.exit(1)

    print(BANNER)
    print(f"  Building {entry}...\n")
    _compile(entry, out_dir)

    # Install npm deps
    fe_dir = Path(out_dir) / "frontend"
    if (fe_dir / "package.json").exists():
        print("  ?  Installing npm dependencies...")
        subprocess.run(["npm", "install", "--silent"], cwd=str(fe_dir), check=False,
                       shell=(sys.platform == "win32"))
        print("  ?  Building React bundle...")
        subprocess.run(["npm", "run", "build", "--silent"], cwd=str(fe_dir), check=False,
                       shell=(sys.platform == "win32"))

    print(f"\n  OK  Build complete -> {out_dir}/\n")

    if analyze:
        print("  [ANALYZE] Bundle Size Report\n")
        import os
        dist_path = Path(out_dir)
        total_bytes = 0
        file_sizes = []
        for root, _, files in os.walk(dist_path):
            for fname in files:
                fpath = Path(root) / fname
                size = fpath.stat().st_size
                total_bytes += size
                rel = fpath.relative_to(dist_path)
                file_sizes.append((size, str(rel)))
        file_sizes.sort(reverse=True)
