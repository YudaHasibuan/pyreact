def cmd_secrets(args):
    import json
    secrets_file = Path(".pyreact_secrets.json")
    if secrets_file.exists():
        try:
            with open(secrets_file, "r", encoding="utf-8") as f:
                secrets = json.load(f)
        except Exception:
            secrets = {}
    else:
        secrets = {}

    if not args:
        print("  Usage: pyreact secrets [list | set <KEY> <VALUE> | remove <KEY>]")
        return

    subcmd = args[0]
    if subcmd == "list":
        print("\n  === PyReact Cloud Secrets ===")
        if not secrets:
            print("    No secrets defined.")
        else:
            for k, v in secrets.items():
                masked = v[:2] + "*" * max(3, len(v) - 2) if len(v) > 2 else "***"
                print(f"    {k} = {masked}")
        print()
    elif subcmd == "set":
        if len(args) < 3:
            print("  Error: Missing KEY or VALUE. Usage: pyreact secrets set <KEY> <VALUE>")
            return
        key = args[1].upper()
        val = args[2]
        secrets[key] = val
        with open(secrets_file, "w", encoding="utf-8") as f:
            json.dump(secrets, f, indent=2)
        print(f"  [SUCCESS] Secret '{key}' updated successfully.")
    elif subcmd in ("remove", "delete"):
        if len(args) < 2:
            print("  Error: Missing KEY. Usage: pyreact secrets remove <KEY>")
            return
        key = args[1].upper()
        if key in secrets:
            del secrets[key]
            with open(secrets_file, "w", encoding="utf-8") as f:
                json.dump(secrets, f, indent=2)
            print(f"  [SUCCESS] Secret '{key}' removed successfully.")
        else:
            print(f"  [WARN] Secret '{key}' not found.")
    else:
        print(f"  Error: Unknown secrets subcommand '{subcmd}'")


def cmd_domain(args):
    import json
    domains_file = Path(".pyreact_domains.json")
    if domains_file.exists():
        try:
            with open(domains_file, "r", encoding="utf-8") as f:
                domains = json.load(f)
        except Exception:
            domains = []
    else:
        domains = []

    if not args:
        print("  Usage: pyreact domain [list | add <domain> | remove <domain>]")
        return

    subcmd = args[0]
    if subcmd == "list":
        print("\n  === PyReact Custom Domains ===")
        if not domains:
            print("    No custom domains configured.")
        else:
            for d in domains:
                print(f"    - {d} (Status: Active, SSL: Verified)")
        print()
    elif subcmd == "add":
        if len(args) < 2:
            print("  Error: Missing domain name. Usage: pyreact domain add <domain>")
            return
        domain = args[1].lower()
        if domain not in domains:
            domains.append(domain)
            with open(domains_file, "w", encoding="utf-8") as f:
                json.dump(domains, f, indent=2)
            print(f"  [SUCCESS] Custom domain '{domain}' added successfully.")
            print(f"  -> Please point your DNS CNAME record to: cname.pyreact.cloud")
        else:
            print(f"  [WARN] Domain '{domain}' is already configured.")
    elif subcmd in ("remove", "delete"):
        if len(args) < 2:
            print("  Error: Missing domain name. Usage: pyreact domain remove <domain>")
            return
        domain = args[1].lower()
        if domain in domains:
            domains.remove(domain)
            with open(domains_file, "w", encoding="utf-8") as f:
                json.dump(domains, f, indent=2)
            print(f"  [SUCCESS] Domain '{domain}' removed.")
        else:
            print(f"  [WARN] Domain '{domain}' not found.")
    else:
        print(f"  Error: Unknown domain subcommand '{subcmd}'")


