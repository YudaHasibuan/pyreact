def wt(path: Path, text: str):
    """Write text to path with UTF-8 encoding, only if content has changed."""
    if path.exists():
        try:
            if path.read_text(encoding="utf-8") == text:
                return
        except Exception:
            pass
    path.write_text(text, encoding="utf-8")

