class PyToJsTranspiler(ast.NodeVisitor):
    def __init__(self):
        self.indent_level = 1

    def transpile(self, source: str) -> str:
        try:
            tree = ast.parse(source)
            return self.visit(tree)
        except Exception as e:
            return f"// Transpilation error: {e}\n" + "\n".join(f"// {l}" for l in source.splitlines())

    def visit_Module(self, node):
        return "\n".join(self.visit(stmt) for stmt in node.body)

    def visit_Assign(self, node):
        indent = "  " * self.indent_level
        target = self.visit(node.targets[0])
        val_node = node.value

        is_server_call = False
        if isinstance(val_node, ast.Call):
            func_str = self.visit(val_node.func)
            if func_str.startswith("server."):
                is_server_call = True

        val = self.visit(val_node)
        
        if is_server_call:
            return f"{indent}const {target} = await {val};"
        else:
            return f"{indent}let {target} = {val};"

    def visit_Expr(self, node):
        indent = "  " * self.indent_level
        val = self.visit(node.value)
        is_server_call = False
        if isinstance(node.value, ast.Call):
            func_str = self.visit(node.value.func)
            if func_str.startswith("server."):
                is_server_call = True

        if is_server_call:
            return f"{indent}await {val};"
        return f"{indent}{val};"

    def visit_Call(self, node):
        func = self.visit(node.func)
        args = [self.visit(arg) for arg in node.args]
        
        if func.startswith("server."):
            if len(args) == 0:
                return f"{func}({{}})"
            else:
                keywords_str = ", ".join(args)
                return f"{func}({{{keywords_str}}})"

        args_str = ", ".join(args)
        return f"{func}({args_str})"

    def visit_Name(self, node):
        mapping = {"None": "null", "True": "true", "False": "false"}
        return mapping.get(node.id, node.id)

    def visit_Constant(self, node):
        if node.value is None:
            return "null"
        if isinstance(node.value, bool):
            return "true" if node.value else "false"
        if isinstance(node.value, str):
            return repr(node.value)
        return str(node.value)

    def visit_Num(self, node):
        return str(node.n)

    def visit_Str(self, node):
        return repr(node.s)

    def visit_NameConstant(self, node):
        mapping = {None: "null", True: "true", False: "false"}
        return mapping.get(node.value, str(node.value))

    def visit_Attribute(self, node):
        value = self.visit(node.value)
        return f"{value}.{node.attr}"

    def visit_If(self, node):
        indent = "  " * self.indent_level
        test = self.visit(node.test)
        
        self.indent_level += 1
        body_stmts = [self.visit(stmt) for stmt in node.body]
        self.indent_level -= 1
        
        body_str = "\n".join(body_stmts)
        out = f"{indent}if ({test}) {{\n{body_str}\n{indent}}}"
        
        if node.orelse:
            if len(node.orelse) == 1 and isinstance(node.orelse[0], ast.If):
                elif_stmt = node.orelse[0]
                elif_rendered = self.visit(elif_stmt).strip()
                out += f" else {elif_rendered}"
            else:
                self.indent_level += 1
                else_stmts = [self.visit(stmt) for stmt in node.orelse]
                self.indent_level -= 1
                else_str = "\n".join(else_stmts)
                out += f" else {{\n{else_str}\n{indent}}}"
            
        return out

    def visit_Compare(self, node):
        left = self.visit(node.left)
        ops = {
            ast.Eq: "===",
            ast.NotEq: "!==",
            ast.Lt: "<",
            ast.LtE: "<=",
            ast.Gt: ">",
            ast.GtE: ">=",
            ast.In: "includes",
        }
        
        parts = [left]
        for op, comparator in zip(node.ops, node.comparators):
            op_str = ops.get(type(op), "==")
            comp_str = self.visit(comparator)
            if op_str == "includes":
                return f"{comp_str}.includes({left})"
            parts.append(op_str)
            parts.append(comp_str)
            
        return " ".join(parts)

    def visit_BinOp(self, node):
        left = self.visit(node.left)
        right = self.visit(node.right)
        ops = {
            ast.Add: "+",
            ast.Sub: "-",
            ast.Mult: "*",
            ast.Div: "/",
            ast.Mod: "%",
        }
        op_str = ops.get(type(node.op), "+")
        return f"({left} {op_str} {right})"

    def visit_BoolOp(self, node):
        op_str = "&&" if isinstance(node.op, ast.And) else "||"
        vals = [self.visit(val) for val in node.values]
        return f"({f' {op_str} '.join(vals)})"

    def visit_UnaryOp(self, node):
        operand = self.visit(node.operand)
        if isinstance(node.op, ast.Not):
            return f"!({operand})"
        if isinstance(node.op, ast.USub):
            return f"-({operand})"
        return operand

    def visit_List(self, node):
        elts = [self.visit(elt) for elt in node.elts]
        return f"[{', '.join(elts)}]"

    def visit_Dict(self, node):
        items = []
        for k, v in zip(node.keys, node.values):
            k_str = self.visit(k)
            if k_str.startswith("'") and k_str.endswith("'"):
                k_str = k_str[1:-1]
            elif k_str.startswith('"') and k_str.endswith('"'):
                k_str = k_str[1:-1]
            v_str = self.visit(v)
            items.append(f"{k_str}: {v_str}")
        return f"{{{', '.join(items)}}}"

    def visit_Subscript(self, node):
        value = self.visit(node.value)
        slice_val = self.visit(node.slice)
        return f"{value}[{slice_val}]"

    def visit_Index(self, node):
        return self.visit(node.value)

    def visit_FunctionDef(self, node):
        indent = "  " * self.indent_level
        args_list = [arg.arg for arg in node.args.args]
        args_str = ", ".join(args_list)
        self.indent_level += 1
        body_stmts = [self.visit(stmt) for stmt in node.body]
        self.indent_level -= 1
        body_str = "\n".join(body_stmts)
        return f"{indent}async function {node.name}({args_str}) {{\n{body_str}\n{indent}}}"

    def visit_Return(self, node):
        indent = "  " * self.indent_level
        val = self.visit(node.value) if node.value else ""
        return f"{indent}return {val};"
