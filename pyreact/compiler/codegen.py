    def _gen_pydantic_schemas(self) -> str:
        if not self.ast.server or not self.ast.server.functions:
            return ""
            
        def translate_type(t_str: str) -> str:
            if not t_str:
                return "Any"
            t_str = re.sub(r'\bString\b', 'str', t_str)
            t_str = re.sub(r'\bNumber\b', 'float', t_str)
            t_str = re.sub(r'\bBoolean\b', 'bool', t_str)
            return t_str

        lines = [
            "from typing import List, Dict, Any, Union, Optional",
            "from pydantic import BaseModel, ValidationError, Field",
            ""
        ]
        for f in self.ast.server.functions:
            lines.append(f"class Schema_{f.name}(BaseModel):")
            if not f.params:
                lines.append("    pass")
            else:
                for p in f.params:
                    p_type = f.param_types.get(p)
                    if p_type:
                        mapped = translate_type(p_type)
                        if "Optional" in mapped or "Union" in mapped:
                            lines.append(f"    {p}: {mapped} = None")
                        else:
                            lines.append(f"    {p}: {mapped}")
                    else:
                        lines.append(f"    {p}: Any = None")
            lines.append("")
        return "\n".join(lines)

    def _gen_ts_declarations(self) -> str:
        def translate_type_to_ts(t_str: str) -> str:
            if not t_str:
                return "any"
            t_str = re.sub(r'\bString\b', 'string', t_str)
            t_str = re.sub(r'\bstr\b', 'string', t_str)
            t_str = re.sub(r'\bNumber\b', 'number', t_str)
            t_str = re.sub(r'\bfloat\b', 'number', t_str)
            t_str = re.sub(r'\bint\b', 'number', t_str)
            t_str = re.sub(r'\bBoolean\b', 'boolean', t_str)
            t_str = re.sub(r'\bbool\b', 'boolean', t_str)
            
            t_str = re.sub(r'\bOptional\[(.*?)\]', r'\1 | null', t_str)
            
            for _ in range(3):
                t_str = re.sub(r'\b(?:List|list)\[(.*?)\]', r'\1[]', t_str)
            
            for _ in range(3):
                t_str = re.sub(r'\b(?:Dict|dict)\[(.*?),\s*(.*?)\]', r'Record<\1, \2>', t_str)
                
            return t_str

        lines = [
            "/**",
            " * PyBridge(tm) Type Declarations.",
            " * Auto-generated by PYREACT compiler.",
            " */",
            "",
            "export interface ServerBridge {"
        ]
        
        if self.ast.server and self.ast.server.functions:
            for f in self.ast.server.functions:
                ret_type = "any"
                if f.return_type:
                    ret_type = translate_type_to_ts(f.return_type)
                
                if not f.params:
                    lines.append(f"  {f.name}(): Promise<{ret_type}>;")
                else:
                    has_required = any(p in f.param_types for p in f.params)
                    payload_name = "payload" + ("" if has_required else "?")
                    
                    fields = []
                    for p in f.params:
                        p_type = f.param_types.get(p)
                        if p_type:
                            ts_type = translate_type_to_ts(p_type)
                            is_opt = "null" in ts_type or "undefined" in ts_type
                            field_name = f"{p}?" if is_opt else p
                            fields.append(f"{field_name}: {ts_type}")
                        else:
                            fields.append(f"{p}?: any")
                    
                    payload_fields = "; ".join(fields)
                    lines.append(f"  {f.name}({payload_name}: {{ {payload_fields} }}): Promise<{ret_type}>;")
        
        lines.append("}")
        lines.append("")
